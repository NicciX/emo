	local timestring = os.date("%m-%d-%Y_%H:%M:%S", os.time())
	timestring = timestring .. "." .. tostring(tx) .. " :: "
	local s
	
	if type(DBG) ~= "number" then
		DBG = 1
	end
	--Game.SendChat("/e EmoBot " .. tostring(DBG))
	--if DBG < 0 then
		--CD["global"].dbg = tostring(DBG)
		--return
	--end
