function type_mark(var)
	local t = type(var)
	if t == "string" then
		return "§"
	elseif t == "number" then
		if var < 1 and var > -1 then --it may look backwards, but should work :P
			return "‰"
		else
			return "π"
		end
	elseif t == "boolean" then
		if var == true then
			return ""
		else
			return "○"
		end
	elseif t == "nil" then
		return ""
	else
		return ""
	end
end

function wrap_safely(hoo, boo)
	if #hoo > 19 then
		hoo = string.sub(hoo,1,17) .. ".."
	end
	if boo == nil then
		return "「"..hoo.."」"
	elseif boo == true then
		return "〔"..hoo.."〕"
	elseif boo == false then
		return "【"..hoo.."】"
	else
		return ""..hoo.." "
	end
end

function tfOrNil(var, tS, fS)
	if var == tS then
		return true
	elseif var ~= fS then
		return false
	else
		return nil
	end
end

