function tobig(txt)
	local s, n
	local ns = txt
	for i=1,#txt,1 do
		s=string.sub(txt, i, i)
		n=string.byte(s)
		if n>64 and n<91 then
			ns = string.gsub(ns, s, string.char(n+57392))
		end
	end
	return ns
end

